def main() -> int:
    text = PO.read_text(encoding="utf-8")
    lines = text.splitlines(keepends=True)
    out = []
    i = 0
    filled = 0
    while i < len(lines):
        line = lines[i]
        # Match a simple single-line msgid followed by an empty msgstr.
        if (
            line.startswith('msgid "')
            and line.endswith('"\n')
            and i + 1 < len(lines)
            and lines[i + 1] == 'msgstr ""\n'
        ):
            msgid = line[len('msgid "') : -2]
            if msgid in TRANSLATIONS:
                translation = TRANSLATIONS[msgid].replace('"', '\\"')
                out.append(line)
                out.append(f'msgstr "{translation}"\n')
                filled += 1
                i += 2
                continue
        out.append(line)
        i += 1
    PO.write_text("".join(out), encoding="utf-8")
    print(f"Filled {filled} entries.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
